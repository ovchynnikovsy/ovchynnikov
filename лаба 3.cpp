#include <iostream>
#include <cmath> // Для математичних функцій, таких як pow
using namespace std;

// Завдання if16: Удвоєння або зміна знаків змінних A, B, C
void task_if16() {
    float A, B, C; // Змінні для введення даних

    cout << "********** Завдання if16 **********" << endl;

    // Введення значень A, B, C з перевіркою
    cout << "Введіть значення A, B і C: ";
    while (!(cin >> A >> B >> C)) { // Перевірка на числове значення
        cout << "Помилка! Введіть три числові значення: ";
        cin.clear();
        cin.ignore(10000, '\n'); // Очищення буфера
    }

    // Перевірка умови зростання значень
    if (A < B && B < C) {
        // Якщо значення впорядковані за зростанням, подвоюємо їх
        A *= 2;
        B *= 2;
        C *= 2;
        cout << "Значення подвоєні: A = " << A << ", B = " << B << ", C = " << C << endl;
    } else {
        // Якщо не впорядковані, змінюємо знаки
        A = -A;
        B = -B;
        C = -C;
        cout << "Значення замінені на протилежні: A = " << A << ", B = " << B << ", C = " << C << endl;
    }
}

// Завдання 34 варіант: Перевірка попадання точки в задану область
void task_34_variant() {
    float x, y, r; // Координати та радіус

    cout << "********** Завдання 34 варіант **********" << endl;

    // Введення радіуса з перевіркою
    cout << "Введіть радіус r: ";
    while (!(cin >> r) || r <= 0) { // Перевірка позитивного числа
        cout << "Помилка! Радіус має бути додатним числом: ";
        cin.clear();
        cin.ignore(10000, '\n');
    }

    // Введення координат точки
    cout << "Введіть координати точки (x, y): ";
    while (!(cin >> x >> y)) { // Перевірка правильного введення координат
        cout << "Помилка! Введіть числові координати: ";
        cin.clear();
        cin.ignore(10000, '\n');
    }

    // Логіка перевірки знаходження в фігурі
    bool in_circle = (x * x + y * y <= r * r) && (x >= 0) && (y >= 0); // Коло в правому верхньому квадранті
    bool in_triangle1 = (x >= 0 && y >= 0 && y <= -x + r); // Правий верхній трикутник
    bool in_triangle2 = (x <= 0 && y >= 0 && y <= x + r);  // Лівий верхній трикутник
    bool in_triangle3 = (x <= 0 && y <= 0 && y >= x - r);  // Лівий нижній трикутник
    bool in_triangle4 = (x >= 0 && y <= 0 && y >= -x - r); // Правий нижній трикутник

    // Результати перевірки
    if (in_circle || in_triangle1 || in_triangle2 || in_triangle3 || in_triangle4) {
        cout << "Точка знаходиться в заданій області!" << endl;
    } else {
        cout << "Точка знаходиться поза заданою областю!" << endl;
    }
}

// Головна функція програми
